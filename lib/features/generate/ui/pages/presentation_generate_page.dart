      shape: const RoundedRectangleBorder(
        borderRadius: BorderRadius.vertical(top: Radius.circular(20)),
      ),
      builder: (bottomSheetContext) => Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          const SizedBox(height: 12),
          // Handle
          Container(
            width: 40,
            height: 4,
            decoration: BoxDecoration(
              color: bottomSheetContext.dividerColor,
              borderRadius: BorderRadius.circular(2),
            ),
          ),
          const SizedBox(height: 16),
          // Title
          Text(
            'Attach Files',
            style: TextStyle(
              fontSize: 18,
              fontWeight: FontWeight.bold,
              color: bottomSheetContext.isDarkMode
                  ? Colors.white
                  : Colors.grey[900],
            ),
          ),
          const SizedBox(height: 8),
          // Options
          ListTile(
            leading: Container(
              width: 40,
              height: 40,
              decoration: BoxDecoration(
                color: Colors.blue.withValues(alpha: 0.1),
                borderRadius: BorderRadius.circular(10),
              ),
              child: const Icon(Icons.description_outlined, color: Colors.blue),
            ),
            title: const Text('Document'),
            subtitle: const Text('PDF, Word, PowerPoint'),
