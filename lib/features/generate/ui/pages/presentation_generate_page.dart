            title: const Text('Link'),
            subtitle: const Text('Website URL'),
            onTap: () {
              Navigator.pop(bottomSheetContext);
              // TODO: Implement link input
              if (mounted) {
                SnackbarUtils.showInfo(context, 'Link input coming soon!');
              }
            },
          ),
          SizedBox(
            height: MediaQuery.of(bottomSheetContext).padding.bottom + 16,
          ),
        ],
      ),
    );
  }

  void _showPickerBottomSheet({required String title, required Widget child}) {
    final isDark = Theme.of(context).brightness == Brightness.dark;

    WoltModalSheet.show<void>(
      context: context,
      modalTypeBuilder: (context) => WoltModalType.bottomSheet(),
      useSafeArea: true,
      pageListBuilder: (modelBottomSheetContext) {
        return [
          SliverWoltModalSheetPage(
            trailingNavBarWidget: IconButton(
              onPressed: () => Navigator.pop(modelBottomSheetContext),
              padding: const EdgeInsets.symmetric(horizontal: 16),
              icon: Icon(
                Icons.close_rounded,
                color: isDark ? Colors.grey[400] : Colors.grey[600],
              ),
            ),
            pageTitle: Center(
              child: Padding(
                padding: const EdgeInsets.symmetric(horizontal: 16),
                child: Text(
                  title,
                  style: TextStyle(
                    color: isDark ? Colors.white : Colors.grey[900],
                    fontSize: 18,
                    fontWeight: FontWeight.bold,
                  ),
                ),
              ),
            ),
            mainContentSliversBuilder: (context) => [
              SliverPadding(
                padding: const EdgeInsets.all(20),
                sliver: SliverToBoxAdapter(child: child),
              ),
            ],
          ),
        ];
      },
    );
  }
}
